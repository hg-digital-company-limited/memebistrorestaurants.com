                                                    alt="" width="395" height="380" /></div>
                                        </div>
                                        <p>&nbsp;</p>
                                        <h5>There are three types of cooking: Steam, convection, and combination</h5>
                                        <p>Three cooking modes are available for commercial combi ovens to achieve all
                                            the functions: steam, convection, and a combination steam/convection.<br />
                                            Steam Mode This steamer mode uses steam heat to keep products moist during
                                            cooking. This is ideal for poaching and steaming vegetables and seafood.</p>
                                        <p>Convection Mode combi oven Hot Air is forced around an oven cavity, just like
                                            in convection ovens. This creates a dry heat that browns food and removes
                                            moisture. This mode is great for baking, oven-frying, and roasting foods
                                            such as cookies, French fries, and pizza.</p>
                                        <blockquote>
                                            <p>There is a way out of every box, a solution to every puzzle; its just a
                                                matter of finding it.</p>
                                        </blockquote>
                                        <p>Combination Mode Steam and hot air combine to make food without losing any
                                            moisture. Its a great choice for stewing or braising foods. Vegetables and
                                            meats will be tender and juicy, but they will not brown as well in this
                                            oven. You can bake certain loaves of bread in this manner, as the steam will
                                            help to form a hard crust.</p>
